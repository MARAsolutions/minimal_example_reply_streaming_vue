<script>
import Pusher from 'pusher-js'
import { v4 as uuidv4 } from 'uuid'


export default {
    name: 'ReviewReply',
    data() {
        return {
            input: 'Tanto las instalaciones como el personal estuvieron muy por encima de nuestras expectativas. La ubicaciÃ³n de este hotel es estratÃ©gica, en el centro de los lugares que puedes visitar... y si no, el transporte estaba a unos pasos. La Ãºnica pega fue, que faltaba un desayuno vegano.',
            channelName: '',
            channel: [],
            messages: '',
            metadata: {},
            isGenerating: false,
            isCopied: false,
            apiUrl: '',
            jwt: '',
            smartSnippetTopic: 'no vegan breakfast',
            smartSnippetReply: 'We provide vegan breakfast, but you need to tell us in advance.',
            business_type: 'Syte Hotel Mannheim',
            signature: 'Your Syte Hotel Mannheim',
            replyTo: 'Ingo',
            language: null,
        }
    },
    created() {
        const pusher = new Pusher('695dc511475504db883e', {
            cluster: 'eu'
        })

        this.channel = uuidv4()
        console.info('â„¹ï¸ Pusher session:', pusher.sessionID)
        console.info('â„¹ï¸ Channel:', this.channel)
        
        pusher.bind('message', (data) => {
            console.info({ "â„¹ï¸ Incoming stream message": data })
            if (data === '###stream-start###') {
                this.messages = ''
                return
            }
            if (data === '###stream-end###') {
                return
            }

            if (/^\s*\d+\s*$/.test(data)) {
                data = ' ' + data
            }
            if (/^\s*-\s/.test(data)) {
                data = '\n' + data
            }
            this.messages += data
        })

        pusher.bind('metainfo', (data) => {
            if (data === '###stream-start###') {
                this.metadata = {}
                return
            }
            if (data === '###stream-end###') {
                return
            }

            for (const key in data) {
                const value = data[key]
                if (typeof value === 'object') {
                    for (const innerKey in value) {
                        const innerValue = value[innerKey].replaceAll('\n', '<br>')
                        if (!this.metadata[key]) {
                            this.metadata[key] = {}
                        }
                        this.metadata[key][innerKey] = innerValue
                    }
                } else {
                    const newValue = value.replaceAll('\n', '<br>')
                    if (!this.metadata[key]) {
                        this.metadata[key] = newValue
                    }
                }
            }
        })

        pusher.subscribe(this.channel)
    },
    methods: {
        generateReviewReply() {
            this.isGenerating = true
            this.messages = ''
            this.metadata = {}
            fetch(`${this.apiUrl}/reply/reply-to-feedback-stream?channel_name=${this.channel}`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'access-token': this.jwt
                },
                body: JSON.stringify({
                    "input_text": this.input,
                    "user_id": "",
                    "profile_id": "",
                    "reply_model_id": "latest-stable-version",
                    "business_type": this.business_type,
                    "tone_of_voice": 1,
                    "response_length": 1,
                    "reply_language": this.language == null ? null : this.language.toUpperCase(),
                    "reply_to": this.replyTo,
                    "sign_offs": [
                        this.signature
                    ],
                    "custom_topic_replies": [{
                        "custom_reply": this.smartSnippetReply,
                        "review_topic": this.smartSnippetTopic,
                        "id": 0
                    }]
                })
            }).then(response => {
                console.log(response)
            }).catch(error => {
                console.error(error)
            }).finally(() => {
                this.isGenerating = false
            })
        },
        formatValue(value) {
            return value.replace(/\n/g, '<br>')
        },
    },
}
</script>
